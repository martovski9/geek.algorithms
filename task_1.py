    # операции. Если пользователь вводит неверный знак (не '0', '+', '-', '*', '/'), программа должна сообщать об ошибке
    # и снова запрашивать знак операции. Также она должна сообщать пользователю о невозможности деления на ноль, если он
    # ввел его в качестве делителя.
