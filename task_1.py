def my_calc(op, a, b):
    # этой функцией можно пользоваться только из нашего текущего main. Для внешних вызовов может генерить ошибку
    # при делении
    # на 0
    res = 0
    if op == '+':
        res = a + b
    elif op == '-':
        res = a - b
    elif op == '*':
        res = a * b
    else:
        res = a / b

    return res


